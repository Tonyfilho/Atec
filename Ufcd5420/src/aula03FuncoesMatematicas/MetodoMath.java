package aula03FuncoesMatematicas;
import java.math.*;

public class MetodoMath {
	
	public  double mathRaizQuadrada(double x) {
		
		return x = Math.sqrt(x);
	}// fim do metodo
	
	public  double mathRandom(double  x) {
		
		return x = Math.random() * 100;
	}// fim do metodo
	
	public  double mathPotencia(double x, double y) {
		
		return x = Math.pow(x, y);
		
	}// fim do metodo
	
	
	

}
