package aula05RevisãoAntonio;

public class MetodosNovos{
	
	static private String Nome;
	static  private int Tel;
	 static private float Valor;
	
	
	public String getNome() {
		return Nome;
	}
	public void setNome(String nome) {
		this.Nome = nome;
	}
	public int getTel() {
		return Tel;
	}
	public void setTel(int tel) {
		this.Tel = tel;
	}
	public float getValor() {
		return Valor;
	}
	public void setValor(float valor) {
		this.Valor = valor;
	}

	

}// fim da classe
